package Aeropuerto;

public class Ciudad {
	private String nombre;
	private String pais;
	private String latitud;
	private String longitud;

	private String getNombre() {
		return this.nombre;
	}

	private String getPais() {
		return this.pais;
	}

	private String getLatitud() {
		return this.latitud;
	}

	private String getLongitud() {
		return this.longitud;
	}
}